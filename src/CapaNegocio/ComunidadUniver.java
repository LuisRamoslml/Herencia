
package CapaNegocio;


public class ComunidadUniver 
{
    public String cod, ape;
    
    public ComunidadUniver(String c, String a)
    {
        this.cod=c;
        this.ape=a;
        
    }

    public String getCod() {
        return cod;
    }

    public void setCod(String cod) {
        this.cod = cod;
    }

    public String getApe() {
        return ape;
    }

    public void setApe(String ape) {
        this.ape = ape;
    }
    
    
}
